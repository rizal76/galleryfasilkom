			<section id="desc">
				<h3>Description</h3>
				<p>
				Cek Kulkas is a virtual fridge application. 
				The users can put add the groceries they have in their own fridges into this virtual fridge.
				They can put this into advantage by choosing what to cook based on the list of groceries they have in this application.
				The application can also help the user to find recipes based on the ingredients stored in the virtual fridge.
				<br/>
				Additionally, the users can take picture of the dishes and share it to Facebook or Twitter.
				If the users feel unsatisfied with the recipes in the database, they can add their own recipes into the application.
				After the recipe is cooked, the application will automatically dispose or decrease the corresponding groceries from the virtual fridge.
				fridge.
				<br/>
				Application Features:
					<ul>
						<li>Manage personal virtual fridge(add/dispose/edit your groceries)</li>
						<li>Provide a variety of grocery measurement units and the ability to convert from one unit to another</li>
						<li>Search recipes based on a list of ingredients the user want</li>
						<li>Full explanation of recipes, including description, list of ingredients, and steps to make them.</li>
						<li>Sort recipes by category</li>
						<li>Create own recipes</li>
						<li>Edit existing recipes</li>
						<li>Flag recipes as favorite recipes</li>
						<li>Share to Facebook and Twitter</li>
						<li>Auto adjustment for groceries</li>
						<li>Cook!</li>
					</ul>
				</p>
