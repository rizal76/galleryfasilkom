				<h3>Description</h3>
				<p>
				The society is often confused by the amount of monthly electric bill. The use of electricity every month seems the same, however, the amount of bills are significantly different. A detailed information about the reason for bill overrun often can not be obtained. Then most of the citizen assuming that they might be spending too much electricity. The situation above motivates the team to develop Veda.
				</p>
				<p>
				Veda is an electronic solution for Smarthome and Energy Monitoring platform. It is a complete automated solution to save electricity easily. Veda supports many devices and applications in monitoring and controlling electronic equipments in houses and buildings. Veda enables other home monitoring devices to be combined with Vedaâ€™s platform. Veda also enables third party developers to install their applications and interact with the devices and environment available in Veda platform. In addition, Veda is designed to be connected to any social media that is packed with real time data and user experience using the internet connection.
				</p>
