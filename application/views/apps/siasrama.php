			<section id="desc">
				<h3>Description</h3>
				<p>
				Asrama UI is a facility provided by the Universitas Indonesia to serve students coming from outside Jabodetabek as a university dormitory for them to live in their first year.
				However, as many other dormitories, Asrama UI has limited rooms, which the number of the rooms are less than the number of students who want to live there. 
				The registration process in Asrama UI used online registration method to collect the students' data, then the selection process is conducted manually.
				The long selection process causes the registration phase takes longer time.
				</p>
				<p>
				The Asrama UI information system was intended to automate the registration and selection process.
				The features available in this information system are as follows:<br/>
				<ol>
					<li>Provide information about Asrama UI</li>
					<li>Register for Asrama UI inhabitant candidate </li>
					<li>Conduct selection for Asrama UI inhabitant candidate</li>
					<li>Collect inhabitant and facilities data in Asrama UI</li>
					<li>Give and handle complaints</li>
					<li>Create financial report of Asrama UI</li>
					<li>Determine and provide information about payment</li>
					<li>Integrated with the JUITA UI account</li>
				</ol>
				</p>
