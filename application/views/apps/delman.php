			<section id="desc">
				<h3>Description</h3>
				<p>
				DelMan atau Delivery Manager adalah sebuah applikasi berbasis Android. Aplikasi ini mempunyai fungsi untuk memudahkan pengguna dalam melihat katalog menu makanan sampai melakukan panggilan pesan antar dari beberapa restoran yang menyediakan layanan pesan antar. Katalog menu makanan yang ditampilkan tak hanya dilengkapi dengan nama dan harga makanan, namun juga dilengkapi dengan deskripsi singkat dan gambar dari makanan tersebut. Jika pengguna telah selesai membuat daftar pesanan, maka akan ditampilkan total aproksimasi biaya yang dibutuhkan. Aproksimasi ini sudah termasuk PPN 10%, namun biaya tambahan seperti biaya antar tidak termasuk dalam aproksimasi tersebut.<p>
Dalam versi ini, DelMan hanya bisa digunakan untuk melakukan panggilan pesan antar di area Jabodetabek. Namun fitur-fitur selain membuat panggilan dapat digunakan di manapun tanpa masalah.
<p>NOTES:<br />SEMUA GAMBAR LOGO RESTORAN DAN MENU MAKANAN YANG TERDAPAT DALAM APLIKASI INI DILINDUNGI HAK CIPTA DARI MASING-MASING RESTORAN. ADAPUN PERIZINAN DARI MASING-MASING RESTORAN MASIH DALAM PROSES KONFIRMASI.
<p>
SEMUA PIHAK RESTORAN YANG DICANTUMKAN DALAM DELMAN TIDAK MEMILIKI TANGGUNG JAWAB ATAS ASPEK APAPUN DALAM APLIKASI INI, KECUALI SAAT PANGGILAN TELAH BERHASIL MASUK KE LAYANAN PESAN ANTAR MASING-MASING RESTORAN.
												</p>
				<p>Main feature:</br>
					<ol>
						<li>Tap to Call<br />Pengguna dapat melakukan panggilan telepon ke restoran yang telah dipilih tanpa harus memasukkan nomor telepon restoran tersebut.</li>
						
						<li>Sharing Calculator<br />Pengguna dapat melakukan penghitungan biaya per orang. Applikasi ini menyediakan 2 opsi untuk melakukan penghitungan tersebut.</li>
						
						<li>Even Share Calculator<br />Opsi ini melakukan penghitungan dengan cara membagi rata total aproksimasi biaya yang diperlukan dengn jumlah orang yang berpartisipasi dalam membuat daftar pesanan tersebut.</li>
						
						<li>Order per Person Calculator<br />Opsi ini melakukan penghitungan bahwa total aproksimasi biaya yang dibutuhkan sebanding dengan apa yang dimakan oleh orang tersebut. Adapun makanan yang dimiliki bersama dapat dialokasikan ke orang - orang yang memakan makanan tersebut.</li>
						
						<li>Favourite<br />Pengguna dapat memberikan label favourite pada menu makanan yang dipilih pengguna. Setelah itu, pengguna dapat melihat daftar favorit mereka melalui menu Favourite yang ditampilkan berdasarkan restorannya sehingga pengguna dapat dengan mudah membuat daftar makanan yang akan dipesan, yang salah satunya adalah makanan favorit. Jika ada menu yang tidak terdaftar dalam daftar favorit, maka pengguna dapat melakukan penambahan menu ke dalam daftar pesanan melalui tombol yang telah tersedia.</li>
						
						<li> Most Recent<br />Pengguna dapat melihat daftar 3 pesanan terbaru yang diurutkan berdarkan tanggal dan waktu pemesanan. Selain itu, pengguna juga dapat membuat daftar makanan yang akan dipesan melalui fungsi Most Recent ini. Jika ada menu yang tidak terdaftar dalam recent, maka pengguna dapat melakukan penambahan menu ke dalam daftar pesanan melalui tombol yang telah tersedia.</li>
						
						<li>Feedback<br />Pengguna dapat mengirimkan umpan balik kepada tim pengembang melalui email yang telah diatur di dalam applikasi ini, sehingga pengguna dapat dengan mudah mengirimnya. Adapun umpan balik tersebut berupa pemberitahuan mengenai perubahan harga atau menu makanan pada restoran tertentu ataupun pemberitahuan mengenai kekurangan, saran dan kritik mengenai applikasi ini. Fitur ini memerlukan koneksi internet untuk mengirimkan email tersebut.</li>
						
					</ol>
				</p>
